# functions of string
s="Hello World"
# len(s) = result the length of a string
print(len(s))
# s.lower() = converts all characters in the string to lowercase
print(s.lower())
# s.upper()= converts all characters in the string to uppercase
print(s.upper())
# s.capitalize = capitalize the first character of a string
print(s.capitalize())
# s.title()= capitalize the first character of each word in a string
print(s.title())

print(s.find("L"))
print(s.replace("L",""))
print(s.startswith("L"))
print(s.endswith("L"))
print(s.isdigit())
print(s.isalpha())
print(s.count("L"))
